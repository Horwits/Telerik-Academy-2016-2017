function solve(args) {
    var path = args[0].split(',').map(Number),
        n = +args[1],
        jumps = [],
        i = 0,
        j = 0,
        currentJump = 0,
        currentPosition = 0,
        currentTotal = 0,
        maxTotal = -1000,
        visited = [];

    //filling the array
    function fillBooleanArray(visited) {
        for (var i = 0; i < path.length; i += 1) {
            visited[i] = false;
        }
    }

    fillBooleanArray(visited);

    for (i = 2; i < n + 2; i += 1) {

        currentPosition = path[0];
        currentTotal += currentPosition;
        visited[currentJump] = true;

        jumps = args[i].split(',').map(Number);

        while (true) {
            if (j === jumps.length) {
                j = 0;
            }
            currentJump += jumps[j];

            if (currentJump >= path.length || currentJump < 0 || visited[currentJump] === true) {
                break;
            }

            currentPosition = path[currentJump];
            currentTotal += currentPosition;
            visited[currentJump] = true;

            j += 1;
        }

        if (currentTotal > maxTotal) {
            maxTotal = currentTotal;
        }

        fillBooleanArray(visited);
        currentTotal = 0;
        currentJump = 0;
        j = 0;
    }

    console.log(maxTotal);
}

